use fpas_lexer::Token;
use std::borrow::Cow;

pub(super) fn token_display(t: &Token) -> Cow<'static, str> {
    match t {
        Token::Program => "program".into(),
        Token::Unit => "unit".into(),
        Token::Uses => "uses".into(),
        Token::Const => "const".into(),
        Token::Var => "var".into(),
        Token::Mutable => "mutable".into(),
        Token::Function => "function".into(),
        Token::Procedure => "procedure".into(),
        Token::Begin => "begin".into(),
        Token::End => "end".into(),
        Token::Return => "return".into(),
        Token::If => "if".into(),
        Token::Then => "then".into(),
        Token::Else => "else".into(),
        Token::Case => "case".into(),
        Token::Of => "of".into(),
        Token::For => "for".into(),
        Token::To => "to".into(),
        Token::Downto => "downto".into(),
        Token::In => "in".into(),
        Token::Do => "do".into(),
        Token::While => "while".into(),
        Token::Repeat => "repeat".into(),
        Token::Until => "until".into(),
        Token::And => "and".into(),
        Token::Or => "or".into(),
        Token::Not => "not".into(),
        Token::Xor => "xor".into(),
        Token::Div => "div".into(),
        Token::Mod => "mod".into(),
        Token::Shl => "shl".into(),
        Token::Shr => "shr".into(),
        Token::True => "true".into(),
        Token::False => "false".into(),
        Token::Type => "type".into(),
        Token::Record => "record".into(),
        Token::Enum => "enum".into(),
        Token::Array => "array".into(),
        Token::Forward => "forward".into(),
        Token::Panic => "panic".into(),
        Token::Break => "break".into(),
        Token::Continue => "continue".into(),
        Token::Public => "public".into(),
        Token::Private => "private".into(),
        Token::Result => "Result".into(),
        Token::OptionKw => "Option".into(),
        Token::Ok => "Ok".into(),
        Token::Error => "Error".into(),
        Token::Some => "Some".into(),
        Token::None => "None".into(),
        Token::Try => "try".into(),
        Token::Go => "go".into(),
        Token::Channel => "channel".into(),
        Token::Select => "select".into(),
        Token::From => "from".into(),
        Token::Dict => "dict".into(),
        Token::Ref => "ref".into(),
        Token::New => "new".into(),
        Token::With => "with".into(),
        Token::Integer(v) => format!("{v}").into(),
        Token::Real(v) => format!("{v}").into(),
        Token::Str(s) => format!("'{s}'").into(),
        Token::Ident(s) => s.clone().into(),
        Token::ColonAssign => ":=".into(),
        Token::DotDot => "..".into(),
        Token::NotEqual => "<>".into(),
        Token::LessEqual => "<=".into(),
        Token::GreaterEqual => ">=".into(),
        Token::Colon => ":".into(),
        Token::Semicolon => ";".into(),
        Token::Comma => ",".into(),
        Token::Dot => ".".into(),
        Token::LParen => "(".into(),
        Token::RParen => ")".into(),
        Token::LBracket => "[".into(),
        Token::RBracket => "]".into(),
        Token::Plus => "+".into(),
        Token::Minus => "-".into(),
        Token::Star => "*".into(),
        Token::Slash => "/".into(),
        Token::Equal => "=".into(),
        Token::Less => "<".into(),
        Token::Greater => ">".into(),
        Token::Eof => "end of file".into(),
    }
}
